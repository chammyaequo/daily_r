# let's initialize some variables
x <- 10
y <- 5

#let's perform some arithmetic operations
sum <- x + y
difference <- x - y
product <- x * y
quotient <- x / y
modulus <- x %% y

# let's print the results
cat("The sum of", x, "and", y, "is", sum, "\n")
cat("The difference between", x, "and", y, "is", difference, "\n")
cat("The product of", x, "and", y, "is", product, "\n")
cat("The quotient of", x, "and", y, "is", quotient, "\n")
cat("The modulus of", x, "and", y, "is", modulus, "\n")