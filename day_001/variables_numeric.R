variable1 <- 100
variable2 <- 20
x <- variable1 + variable2
# This is how we can initialize numeric variables in R.

cat("The value of x is", x, "\n")
# The value of x is 120
# The cat() function is used to concatenate and print the values of variables.
# It can take multiple arguments and will print them in the order they are provided.
# The "\n" at the end is used to add a newline character, so the output appears on a new line.